public class Double extends Exception {

    /**
     * Creates a new instance of <code>Double</code> without detail message.
     */
    public Double() {
        super();
    }

    /**
     * Constructs an instance of <code>Double</code> with the specified detail
     * message.
     *
     * @param msg the detail message.
     */
    public Double(String msg) {
        super(msg);
    }
}
